#include "3-calc.h"
#include <stdio.h>
/**
 * op_add - Function that returns the sum of a and b.
 * @a: Number one.
 * @b: Number two.
 *
 * Return: The sum of a and b.
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - Function that returns the difference of a and b.
 * @a: Number one.
 * @b: Number two.
 *
 * Return: The difference of a and b.
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - Function that returns the product of two numbers.
 * @a: Number one.
 * @b: Number two.
 *
 * Return: The product of a and b.
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - Function that returns the division of two numbers.
 * @a: Number one.
 * @b: Number two.
 *
 * Return: The result
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - Function that returns the remainder of the division of two numbers.
 * @a: Number one.
 * @b: Number two.
 *
 * Return: The remainder.
 */
int op_mod(int a, int b)
{
return (a % b);
}
